public class Contoh1 {
    public static void main(String[] args) {
        
        //Perulangan For
        int x;
        for (x = 1;x <= 5;x++)
        {
            System.out.println("SMK Telkom Malang");
        }
        
        //Perulangan While
        int y = 1;
        while (y <= 5)
        {
            System.out.println("SMK Telkom Malang");
            y++;
        }
        
        //Perulangan Do While
        int z = 1;
        do
        {
            System.out.println("SMK Telkom Malang");
            z++;
        }
        while(z <= 5);
    }
}
